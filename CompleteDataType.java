

public class CompleteDataType {

    public static void main(String[] args) {
        // primitive data types  
        // means it store the value in direct memory
        // int , byte,float,double,boolean,short,long,char 
        int rollNo = 2323230;
        byte age = 25;
        short amount = 10000;
        long totalAmount = 100000033L;
        boolean isValidData = true;
        double idNo = 123456789.5;
        float average = 45.4f;
        char letter = 'A';

        System.out.println("rollNo : " + rollNo);
        System.out.println("age : " + age);
        System.out.println("amount : " + amount);
        System.out.println("totalAmount : " + totalAmount);
        System.out.println("isValidData : " + isValidData);
        System.out.println("idNo : " + idNo);
        System.out.println("average : " + average);
        System.out.println("letter : " + letter);

        // non primitive data types that store memory address instead of value 
        // String , Array , Object, Interfaces, Class 
    }
}
